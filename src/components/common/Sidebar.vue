        data() {
            return {
                items: [
                    {
                        icon: 'el-icon-menu',
                        index: 'userManage',
                        title: '用户管理'
                    },
                    {
                        icon: 'el-icon-menu',
                        index: 'bookManage',
                        title: '书籍管理'
                    },
                ]
            }
        },
        computed:{
            onRoutes(){
                return this.$route.path.replace('/','');
            },
            collapse(){
                return this.$store.state.collapse;
            }
        },
    }
</script>

